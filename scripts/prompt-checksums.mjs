#!/usr/bin/env node
/**
 * prompt-checksums.mjs — MD5 fingerprint of the STOCK Claude Code system
 *                        prompts, for detecting what Anthropic changed/added/
 *                        removed across Claude Code versions.
 *
 * WHY THIS EXISTS
 * ---------------
 * This repo un-nerfs a subset of Claude Code's system prompts. Each un-nerf is
 * a (stock_text -> unnerf_text) rule keyed to a specific prompt. When Anthropic
 * ships a new Claude Code version, any prompt's wording can change, new prompts
 * can appear, and old ones can disappear. A rule whose `stock` text was reworded
 * upstream silently stops applying; a brand-new prompt might introduce a brevity
 * nerf nobody has flipped yet.
 *
 * `git diff` alone can't cleanly surface this, because the files committed in
 * system-prompts/ are the UN-NERFED versions — so a version bump's diff mixes
 * "Anthropic reworded this" together with "my un-nerf is being reverted." This
 * tool fingerprints the *stock* (upstream, unmodified) prompt text instead, so a
 * version bump can answer one precise question: which stock prompts changed,
 * which are new, which are gone — independent of any un-nerf.
 *
 * WHAT IT FINGERPRINTS
 * --------------------
 * The MD5 is taken over the full STOCK `.md` (frontmatter + body) exactly as
 * `sync-version.mjs` / tweakcc would emit it for a freshly installed binary.
 * The per-prompt `ccVersion` frontmatter only changes when that prompt changes,
 * so an untouched prompt has a byte-identical .md (and identical MD5) across CC
 * versions, while any change to body OR metadata flips the hash. That makes the
 * hash a precise per-prompt change detector.
 *
 * IMPORTANT: the manifest stores STOCK hashes, NOT hashes of the un-nerfed files
 * in system-prompts/. Running `--dir system-prompts` against the un-nerfed repo
 * tree will (correctly) report the un-nerfed prompts as "changed" vs stock. The
 * manifest is meant to be written from freshly-generated stock — which is why
 * sync-version.mjs (which holds the stock text in memory before any un-nerf runs)
 * is the normal writer. Point `--dir` at a tweakcc-extracted stock tree only.
 *
 * USAGE
 * -----
 *     # Diff a stock tree against the stored manifest (read-only report):
 *     node scripts/prompt-checksums.mjs --dir <stock-dir>
 *
 *     # Same, and (re)write the manifest for a version:
 *     node scripts/prompt-checksums.mjs --dir <stock-dir> --ccVersion 2.1.185 --write
 *
 *     # CI gate: exit 1 if the stock tree drifts from the manifest:
 *     node scripts/prompt-checksums.mjs --dir <stock-dir> --check
 *
 * sync-version.mjs imports diffManifests / loadManifest / writeManifest /
 * formatDiffReport from here and calls them automatically on every version bump,
 * so the upgrade path reports stock changes/additions/removals without any extra
 * step.
 *
 * EXIT CODES
 * ----------
 *     0  — report printed (and manifest written if --write); no drift in --check
 *     1  — --check found drift, or a filesystem/argument error
 *     2  — invalid invocation
 */

import { createHash } from "node:crypto";
import * as fs from "node:fs/promises";
import * as path from "node:path";
import { fileURLToPath } from "node:url";
import { parseArgs } from "node:util";

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(SCRIPT_DIR, "..");
const DEFAULT_DIR = path.join(REPO_ROOT, "system-prompts");
export const DEFAULT_MANIFEST = path.join(REPO_ROOT, "system-prompt-checksums.json");

const MANIFEST_KIND = "stock-system-prompt-sha256";
const MANIFEST_NOTE =
  "SHA-256 of each STOCK (upstream, unmodified) Claude Code system-prompt .md, " +
  "as produced by sync-version.mjs / our vendored extraction. These are NOT " +
  "hashes of the un-nerfed files committed in system-prompts/. Used to detect " +
  "which prompts Anthropic changed / added / removed across Claude Code " +
  "versions. Regenerated by sync-version.mjs on every version bump.";

// ---------------------------------------------------------------------------
// Core helpers (imported by sync-version.mjs)
// ---------------------------------------------------------------------------

/** Lowercase hex SHA-256 of a string (UTF-8). */
export function sha256(text) {
  return createHash("sha256").update(text, "utf-8").digest("hex");
}
/** @deprecated retained for import compatibility; now SHA-256, not MD5. */
export const md5 = sha256;

/**
 * Build a { "<id>.md": "<sha256>" } map from a list of { id, content } entries.
 * `content` must be the exact stock .md bytes (frontmatter + body).
 */
export function manifestFromContents(entries) {
  const prompts = {};
  for (const { id, content } of entries) {
    prompts[`${id}.md`] = sha256(content);
  }
  return sortKeys(prompts);
}

/** Build a { filename: sha256 } map by hashing every *.md file in `dir`. */
export async function manifestFromDir(dir) {
  const names = (await fs.readdir(dir)).filter((n) => n.endsWith(".md")).sort();
  const prompts = {};
  for (const name of names) {
    const content = await fs.readFile(path.join(dir, name), "utf-8");
    prompts[name] = sha256(content);
  }
  return prompts;
}

/**
 * Compare two { filename: md5 } maps. Returns sorted arrays of filenames.
 * `changed` = present in both with differing hash. `unchanged` is a count-only
 * convenience (the array is returned too, for callers that want it).
 */
export function diffManifests(oldPrompts, newPrompts) {
  oldPrompts = oldPrompts || {};
  newPrompts = newPrompts || {};
  const oldNames = new Set(Object.keys(oldPrompts));
  const newNames = new Set(Object.keys(newPrompts));

  const added = [];
  const removed = [];
  const changed = [];
  const unchanged = [];

  for (const name of newNames) {
    if (!oldNames.has(name)) added.push(name);
    else if (oldPrompts[name] !== newPrompts[name]) changed.push(name);
    else unchanged.push(name);
  }
  for (const name of oldNames) {
    if (!newNames.has(name)) removed.push(name);
  }

  return {
    added: added.sort(),
    removed: removed.sort(),
    changed: changed.sort(),
    unchanged: unchanged.sort(),
  };
}

/** True if the diff shows any structural or content change. */
export function hasDrift(diff) {
  return (
    diff.added.length > 0 || diff.removed.length > 0 || diff.changed.length > 0
  );
}

/** Load a manifest file. Returns null if it doesn't exist. */
export async function loadManifest(manifestPath) {
  try {
    const text = await fs.readFile(manifestPath, "utf-8");
    return JSON.parse(text);
  } catch (err) {
    if (err.code === "ENOENT") return null;
    throw err;
  }
}

/** Write a manifest file with stable key order and a trailing newline. */
export async function writeManifest(manifestPath, { ccVersion, prompts }) {
  const sorted = sortKeys(prompts);
  const manifest = {
    kind: MANIFEST_KIND,
    note: MANIFEST_NOTE,
    ccVersion,
    count: Object.keys(sorted).length,
    prompts: sorted,
  };
  await fs.writeFile(manifestPath, JSON.stringify(manifest, null, 2) + "\n", "utf-8");
  return manifest;
}

/**
 * Render a human-readable diff report. `prevVersion` labels the manifest being
 * compared against; pass null when there is no prior manifest.
 */
export function formatDiffReport(diff, { prevVersion, newVersion } = {}) {
  const lines = [];
  const against = prevVersion ? `vs stock manifest @ v${prevVersion}` : "no prior manifest — establishing baseline";
  const target = newVersion ? ` for v${newVersion}` : "";
  lines.push(`=== Stock system-prompt checksum diff${target} (${against}) ===`);
  lines.push(
    `  changed: ${diff.changed.length}   added: ${diff.added.length}   ` +
      `removed: ${diff.removed.length}   unchanged: ${diff.unchanged.length}`,
  );

  if (diff.changed.length) {
    lines.push("");
    lines.push("CHANGED upstream (re-check any un-nerf rule targeting these):");
    for (const n of diff.changed) lines.push(`  ~ ${n}`);
  }
  if (diff.added.length) {
    lines.push("");
    lines.push("ADDED upstream (review each for a brevity nerf worth flipping):");
    for (const n of diff.added) lines.push(`  + ${n}`);
  }
  if (diff.removed.length) {
    lines.push("");
    lines.push("REMOVED upstream (delete any un-nerf rule targeting these):");
    for (const n of diff.removed) lines.push(`  - ${n}`);
  }
  if (!hasDrift(diff)) {
    lines.push("");
    lines.push("No stock changes since the recorded manifest.");
  }
  return lines.join("\n");
}

function sortKeys(obj) {
  const out = {};
  for (const k of Object.keys(obj).sort()) out[k] = obj[k];
  return out;
}

// ---------------------------------------------------------------------------
// CLI
// ---------------------------------------------------------------------------

function printHelp() {
  console.log(`usage: node prompt-checksums.mjs [options]

Fingerprint a tree of STOCK Claude Code system prompts and diff it against the
stored manifest. Detects which prompts changed / were added / were removed
across Claude Code versions.

options:
  --dir PATH         Directory of stock .md files to hash
                     (default: ${DEFAULT_DIR})
  --manifest PATH    Manifest file to compare against / write
                     (default: ${DEFAULT_MANIFEST})
  --ccVersion V      Version label to stamp into a written manifest (e.g. 2.1.185)
  --write            Write/overwrite the manifest from --dir's hashes.
  --check            Exit 1 if --dir drifts from the manifest. Implies no write.
  -h, --help         Show this help and exit.

NOTE: --dir must point at STOCK prompts (e.g. a tweakcc extraction). Pointing it
at the un-nerfed system-prompts/ tree will report the un-nerfs as "changed".`);
}

async function mainCli(argv) {
  let parsed;
  try {
    parsed = parseArgs({
      args: argv,
      allowPositionals: false,
      options: {
        dir: { type: "string", default: DEFAULT_DIR },
        manifest: { type: "string", default: DEFAULT_MANIFEST },
        ccVersion: { type: "string" },
        write: { type: "boolean", default: false },
        check: { type: "boolean", default: false },
        help: { type: "boolean", short: "h", default: false },
      },
    });
  } catch (err) {
    console.error(`error: ${err.message}`);
    printHelp();
    return 2;
  }

  if (parsed.values.help) {
    printHelp();
    return 0;
  }

  const dir = path.resolve(parsed.values.dir);
  const manifestPath = path.resolve(parsed.values.manifest);

  let newPrompts;
  try {
    newPrompts = await manifestFromDir(dir);
  } catch (err) {
    console.error(`error: could not read prompts from ${dir}: ${err.message}`);
    return 1;
  }
  if (Object.keys(newPrompts).length === 0) {
    console.error(`error: no .md files found in ${dir}`);
    return 1;
  }

  const prior = await loadManifest(manifestPath);
  const diff = diffManifests(prior?.prompts, newPrompts);
  console.error(
    formatDiffReport(diff, {
      prevVersion: prior?.ccVersion ?? null,
      newVersion: parsed.values.ccVersion ?? null,
    }),
  );

  if (parsed.values.write) {
    const ccVersion = parsed.values.ccVersion ?? prior?.ccVersion ?? "unknown";
    await writeManifest(manifestPath, { ccVersion, prompts: newPrompts });
    console.error(`\nwrote manifest: ${manifestPath} (v${ccVersion}, ${Object.keys(newPrompts).length} prompts)`);
  }

  if (parsed.values.check && hasDrift(diff)) {
    console.error("\n--check: stock prompts drifted from the manifest (exit 1)");
    return 1;
  }
  return 0;
}

// Run only when invoked directly (not when imported by sync-version.mjs).
if (path.resolve(fileURLToPath(import.meta.url)) === path.resolve(process.argv[1] ?? "")) {
  process.exitCode = await mainCli(process.argv.slice(2));
}
